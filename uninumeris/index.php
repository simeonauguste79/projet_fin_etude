                    <p>13 courants littéraires : du <strong>classicisme au surréalisme</strong> Chaque année, pour les examens comme le baccalauréat de français en première ou littérature en terminale L, le programme (en « cours de français« ) impose l’étude de différents courants littéraires, avec une œuvre intégrale et la lecture d’ouvrages complémentaires. La notion de courant n’est pas évidente à appréhender pour les élèves, car au sein d’un même mouvement, il peut y avoir plusieurs styles, en fonction de la période et de l’auteur. Un courant littéraire dure entre 20 et 50 ans, ce qui permet aux auteurs d’inscrire leurs œuvres littéraires. Du XVIIème au XIXème siècle, le baroque, le classicisme, les Lumières, le romantisme, le réalisme et le naturalisme se succèdent. Le mouvement littéraire touche également la peinture et la musique. Ces informations seront précieuses pour vous aider à réussir le bac de français et vos réussir vos études littéraires. Passons en revue différents genres littéraires pour en connaître les fondateurs, les œuvres majeures et le contexte culturel dans lequel il s’inscrit. Cet article vous aidera aussi pour vos cours de français.
                    </p>
