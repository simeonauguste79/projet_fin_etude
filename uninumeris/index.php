                    <h5 class="font-weight-bold text-uppercase mb-4">Littérature médiévale</h5>
                    <hr class="bg-primary accent-2 mb- mt-0 d-inline-block mx-auto" style="width: 60px;">
                    <p>On appelle littérature médiévale l'ensemble des œuvres littéraires produites en Occident au cours du Moyen Âge (sur la dizaine de siècles qui s'étend de la chute de l'Empire romain — fin du Ve siècle — au début de la Renaissance florentine — fin du XVe siècle). Cette littérature se compose autant d'œuvres religieuses que séculières, et constitue un champ d'étude riche et complexe. Elle révèle l'existence de nombreuses formes qui contiennent en germe tous les genres littéraires modernes. En raison de sa grande variété et de son étendue dans le temps et l'espace, il est difficile de l'aborder en termes généraux sans d'importantes simplifications, et il est préférable de procéder à son étude par pays/langue et par genre. </p>

                    <h6>Présentation</h6>
                    <hr class="bg-primary accent-2 mt-0 d-inline-block mx-auto" style="width: 50%;">
                    <p>La littérature du Moyen Âge est d'abord celle de l'élite féodale et reflète ses idéaux : piété, fidélité et bravoure. Le système féodal structure alors la société et se reflète dans la littérature : les scènes de guerres y sont nombreuses, la foi chrétienne omniprésente. Néanmoins, à partir de la fin du XIIe siècle, les bourgeois obtiennent, grâce à l'essor de la manufacture, des privilèges économiques et juridiques qui concurrencent les pouvoirs seigneuriaux. On voit apparaître alors de nouvelles formes, plus satiriques comme dans le Roman de Renart, ou plus lyriques comme dans la poésie des XIVe et XVe siècles, héritière de la poésie courtoise. </p>
                    <p>La plupart des auteurs de cette époque nous sont inconnus ; cet anonymat n'est pas simplement causé par le manque de documents disponibles pour la période, mais aussi par une conception du rôle de l'auteur qui diffère totalement de la conception romantique actuelle. Les auteurs médiévaux se réfèrent très souvent aux antiques et aux Pères de l'Église, et tendent plus à remettre en forme ou à embellir les histoires déjà lues ou entendues qu'à en inventer de nouvelles. Même lorsqu'ils le font, ils attribuent fréquemment leur œuvre à un tiers illustre ou imaginaire. On ignore ainsi les noms des auteurs de nombreuses œuvres importantes, notamment pour le Haut Moyen Âge. Le nom des auteurs commence à intéresser le public seulement à partir du XIIe siècle. Dans leur majorité, les textes conservés sont éloignés de la version originale de l'œuvre, parce qu'ils représentent soit la transcription des textes déclamés ou chantés, soit la copie des textes déjà transcrits. Au cours de la diffusion orale d'une œuvre, la « fidélité » à l'auteur, le plus souvent anonyme, reste très aléatoire. D'autre part, les copistes des monastères se permettent des modifications où bon leur semble. Une fois créés, les textes restent donc ouverts : chaque nouveau conteur ou copiste devient coauteur en les modifiant selon ses propres goûts ou les goûts du jour. La littérature médiévale a une réputation médiocre aux XVIe et XVIIe siècles. Au cours de la Renaissance par exemple, elle est traitée de « ténébreuse », d'« obscurantiste », de « barbare ». Au XIXe siècle, les Romantiques la redécouvrent, et l'apprécient à sa juste valeur. Aujourd'hui elle continue d'être lue et réinterprétée. Les mythes qu'elle a créés sont toujours source d'inspiration, comme celui de Tristan et Iseut, fondateur de la conception de l'amour occidental. </p>
